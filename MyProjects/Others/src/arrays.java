import java.util.Scanner;

public class arrays {

	public static void main(String[] args) {
		
		Scanner scan = new Scanner(System.in);
		
		double input = Double.parseDouble(scan.nextLine());
		
	    for (int i = 0; i < input; i++) {
	    	
	    	for (int j = 0; j < input; j++) {
	    		
	    		System.out.print(j);
	    		
	    	}
	    	
	    	System.out.println();
	    }
		
		

	}

}
